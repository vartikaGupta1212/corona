  <h1> 5 Small Steps You Can Take to Protect Yourself from Coronavirus</h1>
   <h2 class="first"><img src="image/sanitizer (1).png" > 1. Practice good hand hygiene.</h2>
    <p>It sounds simple, but it’s true: good hand hygiene helps prevent the transmission of a virus from one person to another.
      Too often, people only wash their hands when they’re visibly soiled. However, viruses and bacteria are
      microscopic, meaning they can be present even if your skin looks clean<span>Washing your hands frequently is a great
        way to avoid a variety of illnesses, not just coronavirus.</span>To keep your hands clean, wash them thoroughly
        using soap and water for at least 20 seconds. Hand-washing frequency will vary from person to person, but it’s
        always a good idea to wash your hands before eating, after using the bathroom, and after sneezing or coughing.</p>
    <h2 class="second " ><img src="image/sneeze.png" > 2.Avoid coughing or sneezing into your hand.</h2>
      <p>Covering a cough or sneeze may seem like something that should be done simply out of good manners, but it
        goes a bit beyond that — you should avoid coughing or sneezing into your hand whenever possible.

       When you cough or sneeze into your hand, you have the potential of transferring bacteria or viruses onto that hand;
       those bacteria and viruses can then be spread to other surfaces you touch.<span>Instead, try to cough or sneeze
         into a tissue. If a tissue isn’t available, try to cover your cough or sneeze using your elbow/upper arm.</span>
         While these methods may not be perfect, they can help prevent the spread of bacteria and viruses. After coughing
          or sneezing into a tissue, throw the tissue away.
           Be sure to wash your hands after a cough or sneeze as well!</p>
      <h2 class="third"><img src="image/home.png" >3. Stay home if you are sick.</h2>
      <p>While it’s admirable to be dedicated to your job or to your studies, it’s not a good idea to go into work or school
         if you’re sick.<span>You may think you can power through the day, but you run the risk of infecting those around you!</span>
         If you’re not feeling well, stay home and let yourself recover. If you have to go out in public, try to wear a face mask
         to avoid infecting others.

         If you’re a parent and your child isn’t feeling well, it’s best to keep him or her home from school. After all,
          illnesses can spread rapidly in school settings, and you don’t want your child to get his or her classmates sick.

          Everyone’s had that familiar feeling of “oh, I probably could have gone in today!” But when you’re sick,
          it’s better to be safe than sorry — your boss or your teacher will thank you for keeping those germs at home!</p>
          <h2 class="fourth"><img src="image/hand.png" > 4. Consider alternatives to shaking hands.</h2>
            <p>Extending a handshake is a common greeting or sign of respect, whether closing a business deal or meeting someone
              for the first time.
          However, as discussed above, your hands can carry bacteria and viruses. This means that shaking someone’s hand comes
          with the risk of passing bacteria or viruses on to that person, or vice versa. Because it’s a respiratory ailment,
          COVID-19 is spread by droplets expelled from the respiratory system by a sneeze or cough. However, if an infected
          person coughs or sneezes into his or her hand, there’s a possibility of transmission through hand-to-hand contact as well.
           In a perfect world where everyone observed the rules of good hand hygiene, shaking hands wouldn’t be as much of a concern.
            However, we all know that many people aren’t exactly diligent about monitoring their hand hygiene.
         In the interest of being careful, </p><span>In the interest of being careful, it’s not a bad idea to cut back on handshakes
           and consider some alternatives for the time being.</span>
           <h2 class="fifth"><img src="image/face.png" >5. Try to avoid touching your face.</h2>
           <p >Touching the hands of other people isn’t the only hand-related practice to lessen: you should try to avoid touching
              your face with your own hands too!As mentioned above, it’s possible for droplets to be transferred via hand-to-hand contact.
              For that reason, it’s a good idea to avoid touching your eyes, nose, and mouth.If droplets containing the virus made their way onto
              your hands, touching your eyes, nose, or mouth would be the primary way to transfer those droplets into your own body.
           Good hand hygiene will lessen the chances of those droplets existing on your hands in the first place, but trying to limit
           how often you touch your face is another good step to take.
          Making these small adjustments will go a long way toward helping you avoid a number of common illnesses, not just coronavirus.
         If you have symptoms that match those of COVID-19 (fever, cough, shortness of breath) and a relevant travel history or close contact
          with a patient with a confirmed case, you should contact your care provider</p>
