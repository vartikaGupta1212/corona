     <h2 >Causes of the New Coronavirus</h2>

     <p >Researchers aren’t sure what caused it. There’s more than one type of coronavirus. They’re common in people and in
      animals including bats, camels, cats, and cattle. SARS-CoV-2, the virus that causes COVID-19, is similar to MERS and
       SARS. They all came from bats. </p>
     <h2>How contagious is the coronavirus?</h2>
      <p>The transmission rate is relatively high. Early research has estimated that one person who has it can spread it to
        between 2 and 2.5 others. One study found that the rate was higher, with one case spreading to between 4.7 and 6.6
        other people. By comparison, one person who has the seasonal flu will pass it to between 1.1 and 2.3 others.

        We can work to lower the transmission rate by washing hands often, keeping common surfaces clean, limiting contact with other
        people, and wearing cloth face masks when we can’t stay 6 feet away from others.</p>
        <h2>Can coronavirus be transmitted through groceries, packages, or food?</h2>
        <p>You’re much more likely to get COVID-19 from another person than from packages, groceries, or food. If you’re in a high-risk
          group, stay home and use a delivery service or have a friend shop for you. Have them leave the items outside your front door,
          if you can. If you do your own shopping, try to stay at least 6 feet away from other shoppers. That isn’t always possible, so
          wear a cloth face mask, too.

          Wash your hands for at least 20 seconds before and after bringing things into your home. The coronavirus can linger on hard
          surfaces, so clean and disinfect countertops and anything else your bags have touched. You can wipe down plastic, metal, or
          glass packaging with soap and water if you want.

          There’s no evidence that anyone has gotten COVID-19 from food or food containers.</p>


