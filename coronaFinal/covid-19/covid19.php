   <h1>How COVID-19 Spreads</h1>
    <p>COVID-19 is thought to spread mainly through close contact from person-to-person. Some people without symptoms
      may be able to spread the virus. We are still learning about how the virus spreads and the severity of illness it causes.</p>
    <h2>Person-to-person spread</h2>
    <ul>The virus is thought to spread mainly from person-to-person.
     <li>Between people who are in close contact with one another (within about 6 feet).</li>
     <li>Through respiratory droplets produced when an infected person coughs, sneezes, or talks.</li>
     <li>These droplets can land in the mouths or noses of people who are nearby or possibly be inhaled into the lungs.</li>
     <li>COVID-19 may be spread by people who are not showing symptoms.</li>
   </ul>
   <h2>What are the symptoms of COVID-19?</h2>
    <ul>Symptoms of COVID-19 include:
     <li>A new, continuous cough, where you cough a lot for more than an hour, or have three or more coughing episodes in 24 hours </li>
     <li>Fever - where your temperature is above 37.8C </li>
     <li>Loss of smell or taste </li>
     <li> Fatigue</li>
     <li>Chills, sometimes with shaking </li>
     <li>Body aches </li>
     <li> Sore throat</li>
     <li>Nausea </li>
     <li>Diarrhea </li>

   </ul>
   <h2>How do I know if it’s COVID-19  or the flu? </h2>
   <ul>
     <li>You have a fever and a cough.</li>
     <li>You live in an area with the virus or have traveled to places where it has spread.</li>
   </ul>
